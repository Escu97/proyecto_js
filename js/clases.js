class Producto{
    constructor(id, nombre, precio, img, oferta){
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.img = img;
        this.oferta = oferta;
    }
}

class Usuario{
    constructor(usuario, contraseña){
        this.usuario = usuario;
        this.contraseña = contraseña;
    }
}